/*1040. 有几个PAT（25）

字符串APPAPT中包含了两个单词“PAT”，其中第一个PAT是第2位(P),第4位(A),第6位(T)；第二个PAT是第3位(P),第4位(A),第6位(T)。

现给定字符串，问一共可以形成多少个PAT？

输入格式：

输入只有一行，包含一个字符串，长度不超过10^5，只包含P、A、T三种字母。

输出格式：

在一行中输出给定字符串中包含多少个PAT。由于结果可能比较大，只输出对1000000007取余数的结果。

输入样例：
APPAPT
输出样例：
2
*/
#include <cstdio>
#include <cstring>
int main()
{
  int leftNumP[100010],rightNumT=0,sum=0;
  char str[100010];
  gets(str);
  int len=strlen(str);
  if(str[0]=='P') leftNumP[0]=1;
  else leftNumP[0]=0;
  for(int i=1;i<len;i++)
  {
    if(str[i]=='P') leftNumP[i]=leftNumP[i-1]+1;
    else leftNumP[i]=leftNumP[i-1];
  }
  for(int i=len-1;i>=0;i--)
  {
    if(str[i]=='T') rightNumT++;
    if(str[i]=='A') sum=(sum+leftNumP[i]*rightNumT)%1000000007;
  }
  printf("%d",sum);
  return 0;
}
